public class Day3Part1 {
    public static void main(String[] args) {
        // Automatic Typecasting
        int a = 10;
        double b = a;
        System.out.println(a);
        System.out.println(b);

        // Explicit Typecasting
        double d = 34.56;
        System.out.println(d);
        int e = (int) d;
        System.out.println(e);
    }
}
